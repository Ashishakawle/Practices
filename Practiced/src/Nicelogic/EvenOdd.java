package Nicelogic;

public class EvenOdd {

	public static void main(String[] args) {
		for(int i=0;i<=10;i++) {
			if(i%2==0) {
				System.out.println("It's Even number"+i);
			}
		}
		System.out.println();
        for(int j=0;j<=10;j++) {
        	if(j%2 !=0) {
        	System.out.println("It's odd number"+j);	
        	}
        }
	}

}
